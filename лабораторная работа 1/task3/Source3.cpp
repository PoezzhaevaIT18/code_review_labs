#include <iostream>

/*
        Коротко о сути:
        Мы считаем, сколько K‑ичных чисел длины N содержат
        4 и более подряд идущих нулей.

        Чтобы не перебирать все числа, мы отслеживаем только то,
        сколько нулей стоит в конце числа (0,1,2,3,≥4).
        На каждом шаге добавляем новый разряд и обновляем состояния.
    */

ZeroResult solve_k_numbers(int k, int n) {
    // FIX_ME: отсутствовала проверка входных данных
    // старый код:
    // if (k < 2 || k > 10) {}
    //
    // новый код:
    if (k < 2 || k > 10 || n <= 1 || n >= 20 || (n + k) >= 26) {
        std::cout << "Ошибка: некорректные входные данные\n";
        return { 0 };
    }

   
    double nz = k - 1;  // нет нулей на конце
    double oz = 1;      // один ноль
    double tz = 0;      // два нуля
    double thz = 0;     // три нуля
    double foz = 0;     // четыре и более нулей

   
    for (int i = 2; i <= n; i++) {
        double _nz = nz;
        double _oz = oz;
        double _tz = tz;
        double _thz = thz;
        double _foz = foz;

        // FIX_ME: логическая ошибка — умножалось на k вместо (k - 1)
        // старый код:
        // nz = (_nz + _oz + _tz + _thz + _foz) * k;
        //
        // новый код:
        nz = (_nz + _oz + _tz + _thz + _foz) * (k - 1);

        oz = _nz;      // было 0 → стало 1 ноль
        tz = _oz;      // было 1 → стало 2 нуля
        thz = _tz;     // было 2 → стало 3 нуля

        // Добавляем ноль к thz → получаем 4 нуля подряд
        // Добавляем ноль к foz → остаёмся в плохом состоянии
        foz = _thz + _foz * k;
    }

    return { foz };
}
